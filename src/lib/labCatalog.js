function normalizeAlias(value = "") {
  return String(value || "")
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-z0-9]/g, "")
    .trim();
}

function createNumericLab({
  key,
  group,
  displayName,
  aliases = [],
  unit = "",
  plausibilityMin = null,
  plausibilityMax = null,
  expectedRangeText = "",
  panelMemberships = [],
}) {
  return {
    key,
    group,
    displayName,
    aliases,
    resultType: "numeric",
    unit,
    plausibilityMin,
    plausibilityMax,
    expectedRangeText,
    panelMemberships,
  };
}

function createCategoricalLab({
  key,
  group,
  displayName,
  aliases = [],
  allowedValues = [],
  panelMemberships = [],
}) {
  return {
    key,
    group,
    displayName,
    aliases,
    resultType: "categorical",
    allowedValues,
    panelMemberships,
  };
}

