export const LAB_CATALOG_BY_KEY = LAB_CATALOG.reduce((acc, lab) => {
  acc[lab.key] = lab;
  return acc;
}, {});

export const LAB_ALIAS_INDEX = LAB_CATALOG.reduce((acc, lab) => {
  const values = [lab.displayName, ...(lab.aliases || [])];

  values.forEach((alias) => {
    const normalized = normalizeAlias(alias);
    if (!normalized) return;
    acc[normalized] = lab.key;
  });

  return acc;
}, {});

export function findLabByKey(key) {
  return LAB_CATALOG_BY_KEY[key] || null;
}

export function findLabByAlias(alias) {
  const normalized = normalizeAlias(alias);
  if (!normalized) return null;

  const key = LAB_ALIAS_INDEX[normalized];
  if (!key) return null;

  return LAB_CATALOG_BY_KEY[key] || null;
}

export function getPanelLabDefinitions(panelName) {
  const keys = LAB_PANELS[panelName] || [];
  return keys
    .map((key) => LAB_CATALOG_BY_KEY[key])
    .filter(Boolean);
}

export function getGroupedLabCatalog() {
  return LAB_CATALOG.reduce((acc, lab) => {
    if (!acc[lab.group]) acc[lab.group] = [];
    acc[lab.group].push(lab);
    return acc;
  }, {});
}

export function isNumericLab(lab) {
  return lab?.resultType === "numeric";
}

export function isCategoricalLab(lab) {
  return lab?.resultType === "categorical";
}

export function valueLooksSuspicious(lab, value) {
  if (!lab || lab.resultType !== "numeric") return false;
  if (value === null || value === undefined || value === "") return false;

  const numeric = Number(String(value).replace(/,/g, "").trim());
  if (Number.isNaN(numeric)) return false;

  if (lab.plausibilityMin !== null && numeric < lab.plausibilityMin) return true;
  if (lab.plausibilityMax !== null && numeric > lab.plausibilityMax) return true;

  return false;
}